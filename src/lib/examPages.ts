/**
 * examPages.ts
 * Data for Indian government exam photo & signature requirements.
 * Each entry generates one page at /exam-photo-size/[slug]
 * and appears as a row on the hub page /indian-exam-photo-size.
 *
 * ADD MORE EXAMS — just push a new object into examRequirements below.
 * The hub listing and per-exam tool pages update automatically.
 */

export interface ExamPhotoRequirement {
  /** Exam name shown in headings */
  name: string;
  /** Conducting body, e.g. "NTA", "UPSC", "SSC" */
  body: string;
  /** One-line exam category */
  category: string;
  /** Photo pixel width required by the portal */
  photoW: number;
  /** Photo pixel height required by the portal */
  photoH: number;
  /** Maximum photo file size in KB */
  photoMaxKb: number;
  /** Minimum photo file size in KB (some portals reject files that are too small) */
  photoMinKb?: number;
  /** Signature pixel width */
  signW: number;
  /** Signature pixel height */
  signH: number;
  /** Maximum signature file size in KB */
  signMaxKb: number;
  /** Minimum signature file size in KB (some portals reject files that are too small) */
  signMinKb?: number;
  /** Accepted output format */
  format: "jpeg" | "png" | "jpeg_or_png";
  /** Background requirement note */
  background: string;
  /** Extra warnings / notes shown on the page */
  notes: string[];
  /** Official source URL */
  sourceUrl?: string;
  /** Display label for the source URL */
  sourceLabel?: string;
}

export interface ExamPage {
  slug: string;
  exam: ExamPhotoRequirement;
  title: string;
  h1: string;
  description: string;
  intro: string;
}

