// ─── Page builder ─────────────────────────────────────────────────────────────

function slugify(name: string): string {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");
}

function buildExamPage(req: ExamPhotoRequirement): ExamPage {
  const fragment = slugify(req.name);
  const slug = `exam-photo-size/${fragment}`;
  const h1 = `${req.name} Photo & Signature Size`;
  const title = `${req.name} Photo Size — ${req.photoW}×${req.photoH} px, Max ${req.photoMaxKb} KB | Crop & Fix Free`;
  const description = `${req.name} (${req.body}) photo upload requirements: ${req.photoW}×${req.photoH} px, max ${req.photoMaxKb} KB, JPEG format. Crop and compress your photo directly in the browser — no upload, no account, 100% private.`;
  const intro = `The <strong>${req.name}</strong> (${req.body}) portal requires a photo of <strong>${req.photoW}×${req.photoH} px</strong> (max ${req.photoMaxKb} KB, JPEG) and a signature of <strong>${req.signW}×${req.signH} px</strong> (max ${req.signMaxKb} KB). Use the three steps below to crop, resize, and compress both — entirely in your browser, nothing is uploaded.`;

  return { slug, exam: req, title, h1, description, intro };
}

export const examPages: ExamPage[] = examRequirements.map(buildExamPage);

export function getExamPage(slugFragment: string): ExamPage | undefined {
  return examPages.find(
    (p) =>
      p.slug === `exam-photo-size/${slugFragment}` || p.slug === slugFragment,
  );
}

/** Returns the URL slug fragment used after /exam-photo-size/ */
export function examSlugFragment(page: ExamPage): string {
  return page.slug.replace("exam-photo-size/", "");
}
